package com.yugyg.express.impl.kdniao;

public class JeheCompanyInfo {
	private String com;
	private String no;

	public String getCom() {
		return com;
	}

	public void setCom(String com) {
		this.com = com;
	}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	@Override
	public String toString() {
		return "JeheCompanyInfo [com=" + com + ", no=" + no + "]";
	}

}